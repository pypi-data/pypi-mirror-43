def acentos(x):
	b=x
	if 'á' in x:
		b=x.replace('á','a')
	if 'é' in x:
		b=x.replace('é','e')
	if 'í' in x:
		b=x.replace('í','i')
	if 'ó' in x:
		b=x.replace('ó','o')
	if 'ú' in x:
		b=x.replace('ú','u')
	return(b)		
	
